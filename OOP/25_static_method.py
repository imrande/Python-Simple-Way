class ArithmeticOperation:
	@staticmethod
	def addition(a, b): print(f'Sum is {a + b}')

	@staticmethod
	def subtract(a, b): print(f'Subtractiom is {b - a}')

	@staticmethod
	def product(a, b): print(f'Product is {a * b}')

ArithmeticOperation.addition(10, 20)
ArithmeticOperation.product(10, 20)
ArithmeticOperation.subtract(10, 20)